Deck::Deck(int n) {

	numPlayers = n;

}

void Deck::initDeck() {
	string types[4] = {"Spades", "Clubs", "Diamonds", "Hearts"};
	int typeArrCount = 0;
	int cardNumb = 1;
	for (int i = 0; i < 52; i++) {
		if (i % 13 == 0 && i != 0) {
			typeArrCount++;
			cardNumb = 1;
		}
		Card c(types[typeArrCount], cardNumb);
		deck.push_back(c);
		cardNumb++;
		
	}
	


}

void Deck::initPlayers() {

	for (int i = 1; i <= numPlayers; i++) {
		Player p(i);
		players.push_back(p);
	
	}

}

